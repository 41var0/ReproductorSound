    private void bPlayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPlayActionPerformed
        try {

            if (isFileCorrect && ! isPlaying) {

                // Cambiamos el nuevo file que se va a reproducir
                rep.cambiarArchivo(file);
                //Ponemos el volumen bajo
                sVolume.setValue(-24);
                rep.cambiarVolumen(-24);

                // Settea el slider con las nuevas proporciones
                progressMusicDuration.setMaximum(rep.getDuracion());
                System.out.println("Duration of " + rep.getDuracion() + " seconds");
                rep.setSlider(progressMusicDuration);


                // Reproduce la musica
                rep.empezarMusica();
                isPlaying = true;
            }

        } catch (Exception e) {

            // Ahora  NO se puede interactuar con los mandos
            bPlay.setEnabled(false);  // Reproducir la musica
            bStop.setEnabled(false);      // Parar la musica 
            sVolume.setEnabled(false);     // Cambiar el volumen
            System.out.println("\033[91mERROR: \033[94m" + e.getLocalizedMessage());
        }

    }//GEN-LAST:event_bPlayActionPerformed

