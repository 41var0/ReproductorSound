    /**
     * Se muestra un JFileChooser para seleccionar el audio Si es correcto el
 file se muestra de verde, sino de rojo
     *
     * @param evt
     */
    private void bSelectFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSelectFileActionPerformed

        // Si la musica se reproduce se para
        if (isPlaying) {
            rep.pararMusica();
            isPlaying = false;
        }

        // Se crea un JFileChooser para seleccionar el file
        JFileChooser seleccionadorArchivos = new JFileChooser();

        int respuesta = seleccionadorArchivos.showOpenDialog(this);

        if (respuesta == JFileChooser.APPROVE_OPTION) {
            file = seleccionadorArchivos.getSelectedFile();
            System.out.println("File: " + file);

            if (file.getName().endsWith(".wav")) {
                jFileShown.setForeground(Color.GREEN);
                isFileCorrect = true;
                
                
                // Ahora se puede interactuar con los mandos de control
                bPlay.setEnabled(true);  // Reproducir la musica
                bStop.setEnabled(true);      // Parar la musica 
                sVolume.setEnabled(true);     // Cambiar el volumen
                
                

            } else {
                jFileShown.setForeground(Color.RED);
                isFileCorrect = false;
            }

            jFileShown.setText(file.getName());
        }
    }//GEN-LAST:event_bSelectFileActionPerformed

